//条件操作符 （三目操作符） 表达exp1? exp2： exp3  
//表达式exp exp1如果成立 exp2计算 整个结构是 exp2的结果
//exp1不成立 exp3计算 整个表达式结构是exp2的结果


int main()
{
	int a = 0;
	int b = 3;
	int max = 0;

	if (a > b)
		max = a;
	else
		max = b;

	max = a > b ? a : b;
	printf("%d\n", max);
	return 0;
}








