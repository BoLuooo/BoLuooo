#define  _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

int main()
{
	int a = 0;
	printf("%d\n", ~a);//~波浪号  按位取反
	//结果是-1    按（二进制）位取反
//把所有二进制位中的数字，1变成0，0变成1
	//整数 a=0    0的二进制是32个0  二进制：00000000000000000000000000000000
	//~波浪号 按位取反  0变成1  所以上面就是11111111111111111111111111111111 ~a



	//整数在内存中存储的是补码~   一个整数在二进制表示有3种：原码 反码 补码
    //-1的原码  10000000000000000000000000000001（原码） 
	//按位取反 符号位不变 剩下的变 11111111111111111111111111111110(反码)
	//11111111111111111111111111111111（补码） 存的是补码                                   正整数 ：原码反码补码相同   

	//符号位不变 其他位按位取反 反码+1得到补码  补码-1变成反码 反码取反变成原码

	return 0;
}










